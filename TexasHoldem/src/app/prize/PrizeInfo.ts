/**
 * 奖品信息
*/
class PrizeInfo
{
    /**
     * id
    */
    public id: number;
    /**
     * 奖品名称
    */
    public name: string;
    /**
     * 奖品图标
    */
    public icon: number;
    /**
     * 奖品描述
    */
    public des: string;
    /**
     * 效果类型 （用于区分是不是实物）
    */
    public effectType: number;
    /**
     * 奖品状态
    */
    public state: number;
}